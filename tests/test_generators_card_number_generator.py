import pytest

from src.generators import card_number_generator


@pytest.mark.parametrize(
    "start, end, expected_results",
    [
        (
            1,
            5,
            [
                "0000 0000 0000 0001",
                "0000 0000 0000 0002",
                "0000 0000 0000 0003",
                "0000 0000 0000 0004",
                "0000 0000 0000 0005",
            ],
        ),
        (10, 12, ["0000 0000 0000 0010", "0000 0000 0000 0011", "0000 0000 0000 0012"]),
        (9998, 10000, ["0000 0000 0000 9998", "0000 0000 0000 9999", "0000 0000 0001 0000"]),
        (
            9999999999999980,
            9999999999999999,
            [
                "9999 9999 9999 9980",
                "9999 9999 9999 9981",
                "9999 9999 9999 9982",
                "9999 9999 9999 9983",
                "9999 9999 9999 9984",
                "9999 9999 9999 9985",
                "9999 9999 9999 9986",
                "9999 9999 9999 9987",
                "9999 9999 9999 9988",
                "9999 9999 9999 9989",
                "9999 9999 9999 9990",
                "9999 9999 9999 9991",
                "9999 9999 9999 9992",
                "9999 9999 9999 9993",
                "9999 9999 9999 9994",
                "9999 9999 9999 9995",
                "9999 9999 9999 9996",
                "9999 9999 9999 9997",
                "9999 9999 9999 9998",
                "9999 9999 9999 9999",
            ],
        ),
    ],
)
def test_card_number_generator(start: int, end: int, expected_results: list) -> None:
    generator = card_number_generator(start, end)
    results = list(generator)
    assert results == expected_results
